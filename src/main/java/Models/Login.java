package Models;

/**
 *
 * @author Gabriel
 */
public class Login {
    String login;
    String senha;

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
    
    public Login (String login, String senha){
        this.login = login;
        this.senha = senha;
    }
}
